package com.zdh.algorithm;

public class AddOne
{

	public static void main(String[] args) {
		int num1 = 0;
		int num2 = 0;
		
		for (int i = 0; i < 5; i++)
		{
			System.out.println(num1++);
			System.out.println(++num2);
			System.out.println("---------------------------------------------------------");
		}
		
		
	}
}
